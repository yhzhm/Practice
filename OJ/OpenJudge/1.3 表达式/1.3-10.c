/*
10:计算并联电阻的阻值
查看 提交 统计 提问
总时间限制: 1000ms 内存限制: 65536kB
描述
对于阻值为r1和r2的电阻，其并联电阻阻值公式计算如下：

R = 1/(1/r1 + 1/r2)

输入
两个电阻阻抗大小，浮点型，以一个空格分开。
输出
并联之后的阻抗大小，结果保留小数点后2位
样例输入
1 2
样例输出
0.67
*/


#include <stdio.h>
#define PI 3.14159
int main ()
{
	float r1,r2,r;
	scanf("%f%f",&r1,&r2);
	r=1/(1/r1+1/r2);
	printf("%.2f",r);
	return 0;
}
